export function Icon({ name, size = 24, color = "#000", strokeWidth = 1.75 }: IconProps) {
  const spec = ICONS[name];
  if (!spec) return null;

  const paths = Array.isArray(spec.d) ? spec.d : spec.d.split(" M").map((p, i) => (i === 0 ? p : "M" + p));

  if (spec.type === "fill") {
    // Handle multiple space-separated paths in a single string
    const allPaths = typeof spec.d === "string" ? [spec.d] : spec.d;
    return (
      <Svg width={size} height={size} viewBox="0 0 24 24" fill={color}>
        {allPaths.map((d, i) => (
          <Path key={i} d={d} fill={color} />
        ))}
      </Svg>
    );
  }

  // stroke type
  const strokePaths = typeof spec.d === "string" ? [spec.d] : (spec.d as string[]);
  return (
    <Svg width={size} height={size} viewBox="0 0 24 24" fill="none">
      {strokePaths.map((d, i) => (
        <Path
          key={i}
          d={d}
          stroke={color}
          strokeWidth={strokeWidth}
          strokeLinecap="round"
          strokeLinejoin="round"
        />
      ))}
    </Svg>
  );
}
